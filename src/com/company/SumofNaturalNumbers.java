package com.company;

public class SumofNaturalNumbers {
    public static void main(StringPractice[] args) {
        //program to find the sum of 20 natural numbers
        int n = 20;
        int sum = 0;
        for (int i = 1; i <= n; i++) {

            sum = sum + i;

        }

        System.out.println(sum);

    }


}

